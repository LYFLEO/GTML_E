%%%%%%%%%%%%%%%%%%
%   T  [221K~2000K]
%%%%%%%%%%%%%%%%%%
function cp=Cp_T(t,f)

%cp:J/kg/K

%t:K
%f:-

if nargin == 1
    f = 0;
end

if t < 221
    t = 221;
elseif t > 2000
    t = 2000;
end

tz=t/1000;

A0=0.992313;
A1=0.236688;
A2=-1.852148;
A3=6.083152;
A4=-8.893933;
A5=7.097112;
A6=-3.234725;
A7=0.794571;
A8=-0.081873;

B0=-0.718874;
B1=8.747481;
B2=-15.863157;
B3=17.254096;
B4=-10.233795;
B5=3.081778;
B6=-0.361112;
B7=-0.003919;

cp=(A0+A1*tz+A2*tz^2+A3*tz^3+A4*tz^4+A5*tz^5+A6*tz^6+A7*tz^7+A8*tz^8)+((f/(1+f))*(B0+B1*tz+B2*tz^2+B3*tz^3+B4*tz^4+B5*tz^5+B6*tz^6+B7*tz^7));

cp=cp*1e3;

end
